
'''
13. Crea un programa que convierta una temperatura en
grados Celsius a Fahrenheit.
La fórmula que tiene que tener en cuenta es la siguiente:
F = (C * 9)/5 + 32

Deberá imprimir algo como lo siguiente:
La temperatura en °C: 30
Temperatura en Fahrenheit: 86.00

Realizarlo con dos distintos datos para la temperatura en Celsius (usar dos variables iniciales para obtener dos temperaturas finales en Fahrenheit)
'''

var_celcius1 = 30
var_fahrenheit1 = ( ( var_celcius1 * 9 ) / 5 ) + 32

print('\n*** RESULTADO EJERCICIO 13 - 1ER CASO ***\n')
print ("La temperatura en °C es: ", var_celcius1)
print ("La temperatura en Fahrenheit es: ", var_fahrenheit1)


var_celcius2 = 40
var_fahrenheit2 = ( ( var_celcius2 * 9 ) / 5 ) + 32

print('\n*** RESULTADO EJERCICIO 13 - 2DO CASO ***\n')
print ("La temperatura en °C es: ", var_celcius2)
